		<h2><a href="kontakt.php"><i class="fas fa-scroll fa-lg"></i> Tiefenpsychologisch</strong></a></h2>
		<p>Tiefenpsychologisch fundierte Verfahren</p>
		<p>Die tiefenpsychologisch fundierte Psychotherapie beruht auf den theoretischen Grundlagen der Psychoanalyse. Der Wortteil „Tiefe“ in Tiefenpsychologie verweist dabei sowohl auf die Tiefe des Unbewussten (unbewusste oder unverstandene Wünsche, Motive und Konflikte) als auch auf die Tiefe der Zeit, also die fortdauernden Einflüsse aus Kindheit und Jugend.</p>
		<p>Tiefenpsychologie geht davon aus, dass in der Tiefe liegende, unbewusste psychische Vorgänge Wirkung auf die aktuelle psychische Gesundheit des Menschen haben. Unbewusste Konflikte oder verdrängte Erfahrungen sind daher bedeutsam, um psychische Störungen zu behandeln. Im Unterschied zur Verhaltenstherapie liegt der Schwerpunkt damit weniger auf der Beeinflussung des Verhaltens des Patienten, sondern auf einer Klärung der zugrundeliegenden Ursachen.</p>
		<p>Therapeut und Patient sitzen sich in der Regel gegenüber. Eine Couch, wie Freud sie in seiner Psychoanalyse verwendet hat, kommt heute kaum noch zum Einsatz.</p>
