	<section class="spalte sexualtherapie">
		<h2><a href="kontakt.php"><i class="fas fa-scroll fa-lg"></i> Sexualtherapie</strong></a></h2>
		<p>Sexualtherapeutischer Ansatz</p>
		<p>Die Sexualtherapie geht den Problemen im Sexualleben auf den Grund. Sexuelle Probleme können ebenso Symptom einer körperlichen oder psychischen Erkrankung sein, wie auch die Folge (sozialer) Konflikte und einer destruktiven Beziehungsdynamik. Je nach ihrer Genese fällt ihre Behandlung in den Bereich der Psychotherapie bzw. der beratenden Psychologie. Als sexuelle Störung werden Ausprägungen des Sexualverhaltens bzw. des sexuellen Erlebens bezeichnet, die durch den Betroffenen selbst als „Störung“ empfunden werden. Dabei ist es möglich, dass eine derartige Störung nicht als solche erkannt werden kann, weil der Betroffene sich der möglichen Qualität sexueller Entfaltung nicht bewusst ist. Darüber hinaus kann z. B. ein Leidenszustand aufgrund von Partnerlosigkeit als Störung empfunden werden, ein bewusster Verzicht auf einen Partner wird hingegen nicht als Störung eingeschätzt. Insofern der Betroffene seine sexuellen Neigungen bzw. das daraus resultierende Verhalten nicht als Störung empfindet, aber Partner bzw. Gesellschaft diese Vorlieben anders bewerten, so kann dieser Konflikt als Störung wahrgenommen werden. Die angeführten Punkte erschweren eine Darstellung der Verbreitung von sexuellen Störungen.</p>
		<p>Die Unzufriedenheit mit dem eigenen Sexualleben ist weit verbreitet. In einer US-amerikanischen Studie gaben 43 % der Frauen und 35 % der Männer an, unter sexuellen Störungen zu leiden. Besonders häufig sind die männliche Erektile Dysfunktion unterschiedlicher Genese, Orgasmusstörungen sowie Appetenzstörungen. Seltener, aber oft umso dramatischer für den Betroffenen, ist der Bereich der Paraphilien.</p>
		<p>Immer mehr Menschen gehen zur Sexualtherapie. Die Wenigsten können sich vorher denken, was in einer Sexualtherapie auf sie zukommt. </p>
		<p>Eine Sexualtherapie kommt bei Schwierigkeiten des sexuellen Erlebens oder einer Störung des Sexualverhaltens zum Einsatz.</p>
		<ul class="fa-ul">
			<li><i class="fa-li fa fa-check-circle"></i>Meistens kommt im Zuge einer Partnerschaft die Frage nach einer Sexualtherapie auf. Oft wünscht der eine Partner häufiger Sex als der andere und ist dadurch unzufrieden. Auch die Form des sexuellen Auslebens kann eine Rolle spielen.</li>
			<li><i class="fa-li fa fa-check-circle"></i>Bei einigen Menschen lässt die Lust nach Sex stark nach. Erektionsprobleme vor oder während des Geschlechtsaktes können auch ein Anstoß sein, eine solche Therapie in Betracht zu ziehen.</li>
			<li><i class="fa-li fa fa-check-circle"></i>Eine Sexualtherapie ist aber auch sinnvoll, wenn eine traumatische Erfahrung überwunden werden muss, wie z. B. eine Vergewaltigung während der Kindheit. </li>
		</ul>
		<p>Was passiert bei einer Sexualtherapie?</p>
		<ul class="fa-ul">
			<li><i class="fa-li fa fa-check-circle"></i>Die Sexualtherapie wird gemeinsam mit einem Psychologen/Sexualtherapeuten durchgeführt. Zentral ist dabei das Sprechen über die sexuellen Schwierigkeiten.</li>
			<li><i class="fa-li fa fa-check-circle"></i>Zuerst wird eine Diagnose gestellt. Dazu erfragt der Therapeut die Lebensgeschichte, aktuelle Beziehungen und das Sexualleben.</li>
			<li><i class="fa-li fa fa-check-circle"></i>Sind die Ursachen in einer traumatischen Kindheitserfahrung zu suchen, dann kommt eine Traumatherapie zum Einsatz.</li>
			<li><i class="fa-li fa fa-check-circle"></i>Bei Orgasmusstörungen und vorzeitigem Samenerguss werden verschiedene Methoden zur Selbsterforschung, Selbstfindung und Selbstbefriedigung angewendet.</li>
			<li><i class="fa-li fa fa-check-circle"></i>Bei einer Paartherapie steht nicht nur das Kennenlernen des eignen Körpers im Vordergrund, sondern auch das Wahrnehmen des Partners. Es wird auch gelernt, sich über sexuelle Wünsche auszutauschen.</li>
			<li><i class="fa-li fa fa-check-circle"></i>Sollten grundsätzliche Konflikte der sexuellen Erfüllung im Wege stehen, werden diese Schwierigkeiten ebenfalls in der Sexualtherapie behandelt. </li>
		</ul>
