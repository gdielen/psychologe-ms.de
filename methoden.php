		<h2><a href="kontakt.php"><i class="fas fa-scroll fa-lg"></i> Systemische Verfahren</strong></a></h2>
		<p>Systemische (Familien-)Therapie</p>
		<p>Systemische Therapie bezeichnet ein Gruppe psychotherapeutischer Ansätze, das sich zunächst als “Familientherapie” etablierte, dann aber zunehmend unabhängig vom Familien-Setting spezifische Konzepte und therapeutische Vorgehensweisen entwickelte.</p>
		<p>Zentrales Konzept der Methode ist es, Probleme nicht als Störung eines einzelnen Menschen zu begreifen, sondern als Folge einer Störung im sozialen Umfeld des Individuums – also des Systems. Ein System kann beispielsweise die Familie sein, aber auch die Schule oder das Arbeitsumfeld. Die Wechselwirkungen zwischen dem Betroffenen und seinem Umfeld stehen im Fokus der Systemischen Therapie.</p>
		<p>Systemische Therapie ist an Beziehungsprozessen der Personen interessiert, die an der Entstehung und Aufrechterhaltung eines Problems beteiligt und daher auch für Veränderungs- und Lösungsprozesse von Bedeutung sind. Die beteiligten Personen müssen bei einer Systemischen Therapie jedoch nicht unbedingt anwesend sein.</p>
