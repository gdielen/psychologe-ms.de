	<section class="spalte verhaltenstherapie">
		<h2><a href="kontakt.php"><i class="fas fa-scroll fa-lg"></i> Verhaltenstherapie</strong></a></h2>
		<p>Verhaltenstherapie (VT, KVT)</p>
		<p>Mit Verhaltenstherapie wird ein Spektrum von Methoden im Fachgebiet der Psychotherapie bezeichnet, denen gemeinsam ist, das Modell der (klassischen und operanten) Konditionierung als zentral für die Abläufe in der menschlichen Psyche zu bewerten.</p>
		<p>Die klassische Verhaltenstherapie geht zurück auf den amerikanische Psychologe John B. Watson. Verhaltenstherapie legt weniger den Schwerpunkt auf die lebensgeschichtliche Entwicklung des Patienten, es geht vielmehr darum, seine aktuelle Einstellungen zu untersuchen und nach Möglichkeit zu verändern. Anstatt die Ursachen für seelische Probleme in der Kindheit zu suchen, soll der Patient vor allem Techniken für die Gegenwart erlernen, um dadurch mit seinen Belastungen besser umzugehen.</p>
		<p>Die Verhaltenstherapie, kurz VT, heute oft in der Form Kognitiven Verhaltenstherapie (KVT) geht davon aus, dass eine Verhaltensstörung auf einer erlernten Fehleinstellung beruht, die durch gezieltes "Verlernen" wieder aufgehoben werden kann. Ein wesentliches Kennzeichen verhaltenstherapeutischer Verfahren ist es hierbei, den Betroffenen zur Selbsthilfe anzuleiten und ihm Strategien zu vermitteln, die ihn in die Lage versetzen, seinen psychischen Problemen entgegenzutreten.</p>
