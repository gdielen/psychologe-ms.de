		<h2><a href="kontakt.php"><i class="fas fa-scroll fa-lg"></i> Gesprächstherapie</strong></a></h2>
		<p>Gesprächstherapeutische Verfahren</p>
		<p>Die Gesprächspsychotherapie (kurz Gesprächstherapie) ist eine Psychotherapieform der Humanistischen Psychologie. Ihr Vater ist der amerikanische Psychologe Carl R. Rogers. Er hat sich zeitlebens dafür interessiert, wodurch sich eine hilfreiche Beziehung in Therapie und Beratung auszeichnet. Für ihn ist die Haltung des Therapeuten zum Klienten ein wesentliches Element der Therapie. In seiner Gesprächstherapie stellt er sich ganz auf den Klienten ein, der „Experte in eigener Sache“ ist. Der Patient ist in dieser therapeutischen Beziehung „gleichberechtigt”.</p>
		<p>Gesprächstherapie versteht sich als eine Art Instrument, das dem Klienten hilft, durch Selbstexploration Erlebtes angemessen zu verarbeiten und falsches Verhalten durch Einsicht zu ändern. Hierbei steht das Gespräch im Vordergrund. Klassische Anwendungsbereiche sind die sog. neurotischen Erkrankungen, dazu gehören Ängste, Depressionen, psychosomatische Erkrankungen, Sexualstörungen etc. Sie gilt auch als eine erfolgreiche Behandlungsmethode bei Suchterkrankungen, Persönlichkeitsstörungen und psychotischen Erkrankungen.</p>
