		<p><strong>Gesprächstehrapeutische Verfahren</strong></p>
			<p>Die Gesprächspsychotherapie (umgangssprachlich auch Gesprächstherapie), klientenzentrierte Psychotherapie oder klientenzentrierte Gesprächspsychotherapie (auch non-direktive sowie personzentrierte Psychotherapie nach der im Englischen gebräuchlichen Bezeichnung person-centered psychotherapy) ist eine Psychotherapieform der Humanistischen Psychologie.</p>
		<p><strong>Systemische Verfahren</strong></p>
			<p>Zentrales Konzept systemischer Verfahren ist es, Probleme nicht als Störung eines einzelnen Menschen zu begreifen, sondern als Folge einer Störung im sozialen Umfeld des Individuums, also des Systems. Ein System kann beispielsweise die Familie sein, aber auch die Schule oder das Arbeitsumfeld. </p>
		<p><strong>Verhaltenstherapie&nbsp;(VT), Kognitive&nbsp;Verhaltenstherapie (KVT)</strong></p>
			<p>Mit Verhaltenstherapie wird ein Spektrum von Methoden im Fachgebiet der Psychotherapie bezeichnet, die auf der Idee des Lernen und neulernens basieren.</p>
		<p><strong>Tiefenpsychologisch fundierte Verfahren</strong></p>
			<p>Die tiefenpsychologisch fundierte Psychotherapie ist eine Form der Psychotherapie und beruht auf den theoretischen Grundlagen der Psychoanalyse und ihren Weiterentwicklungen.</p>
		<p><strong>Sexualtherapeutischer Ansatz</strong></p>
			<p>Sexualtherapie hat sich als Begriff und als Bezeichnung für die Behandlung von sexuellen Funktionsstörungen etabliert, wobei sich Störungen des Liebes- und Geschlechtslebens allerdings weder krankheitstheoretisch noch behandlungstechnisch von anderen psychosozialen Erkrankungen des Menschen unterscheiden.</p>
